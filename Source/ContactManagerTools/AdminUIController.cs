        public static string getReport2(CPBaseClass cp, int RowCount, string[] ColCaption, string[] ColAlign, string[] ColWidth, string[,] Cells, int PageSize, int PageNumber, string PreTableCopy, string PostTableCopy, int DataRowCount, string ClassStyle, bool[] ColSortable, int DefaultSortColumnPtr) {
            string result = "";
            try {
                string RQS = null;
                int RowBAse = 0;
                var Content = new StringBuilder();
                var Stream = new StringBuilder();
                int ColumnCount = 0;
                int ColumnPtr = 0;
                string ColumnWidth = null;
                int RowPointer = 0;
                string WorkingQS = null;
                //
                int PageCount = 0;
                int pagePtr = 0;
                int LinkCount = 0;
                int ReportPageNumber = 0;
                int ReportPageSize = 0;
                string iClassStyle = null;
                int SortColPtr = 0;
                int SortColType = 0;
                //
                ReportPageNumber = PageNumber;
                if (ReportPageNumber == 0) {
                    ReportPageNumber = 1;
                }
                ReportPageSize = PageSize;
                if (ReportPageSize < 1) {
                    ReportPageSize = 50;
                }
                //
                iClassStyle = ClassStyle;
                if (string.IsNullOrEmpty(iClassStyle)) {
                    iClassStyle = "ccPanel";
                }
                //If IsArray(Cells) Then
                ColumnCount = Cells.GetUpperBound(1);
                //End If
                RQS = cp.Doc.RefreshQueryString;
                //
                SortColPtr = getReportSortColumnPtr(cp, DefaultSortColumnPtr);
                SortColType = getReportSortType(cp);
                //
                // ----- Start the table
                //
                Content.Append(HtmlController.tableStart(3, 1, 0));
                //
                // ----- Header
                //
                Content.Append("\r\n<tr>");
                Content.Append(getReport_CellHeader( 0, "&nbsp", "50px", "Right", "ccAdminListCaption", RQS, SortingStateEnum.NotSortable));
                for (ColumnPtr = 0; ColumnPtr < ColumnCount; ColumnPtr++) {
                    ColumnWidth = ColWidth[ColumnPtr];
                    if (!ColSortable[ColumnPtr]) {
                        //
                        // not sortable column
                        //
                        Content.Append(getReport_CellHeader( ColumnPtr, ColCaption[ColumnPtr], ColumnWidth, ColAlign[ColumnPtr], "ccAdminListCaption", RQS, SortingStateEnum.NotSortable));
                    } else if (ColumnPtr == SortColPtr) {
                        //
                        // This is the current sort column
                        //
                        Content.Append(getReport_CellHeader( ColumnPtr, ColCaption[ColumnPtr], ColumnWidth, ColAlign[ColumnPtr], "ccAdminListCaption", RQS, (SortingStateEnum)SortColType));
                    } else {
                        //
                        // Column is sortable, but not selected
                        //
                        Content.Append(getReport_CellHeader( ColumnPtr, ColCaption[ColumnPtr], ColumnWidth, ColAlign[ColumnPtr], "ccAdminListCaption", RQS, SortingStateEnum.SortableNotSet));
                    }

                    //If ColumnPtr = SortColPtr Then
                    //    '
                    //    ' This column is currently the active sort
                    //    '
                    //    Call Content.Append(GetReport_CellHeader(ColumnPtr, ColCaption(ColumnPtr), ColumnWidth, ColAlign(ColumnPtr), "ccAdminListCaption", RQS, SortColType))
                    //Else
                    //    Call Content.Append(GetReport_CellHeader(ColumnPtr, ColCaption(ColumnPtr), ColumnWidth, ColAlign(ColumnPtr), "ccAdminListCaption", RQS, SortingStateEnum.SortableNotSet))
                    //End If
                }
                Content.Append("\r\n</tr>");
                //
                // ----- Data
                //
                if (RowCount == 0) {
                    Content.Append("\r\n<tr>");
                    Content.Append(getReport_Cell( (RowBAse + RowPointer).ToString(), "right", 1, RowPointer));
                    Content.Append(getReport_Cell( "-- End --", "left", ColumnCount, 0));
                    Content.Append("\r\n</tr>");
                } else {
                    RowBAse = (ReportPageSize * (ReportPageNumber - 1)) + 1;
                    for (RowPointer = 0; RowPointer < RowCount; RowPointer++) {
                        Content.Append("\r\n<tr>");
                        Content.Append(getReport_Cell( (RowBAse + RowPointer).ToString(), "right", 1, RowPointer));
                        for (ColumnPtr = 0; ColumnPtr < ColumnCount; ColumnPtr++) {
                            Content.Append(getReport_Cell( Cells[RowPointer, ColumnPtr], ColAlign[ColumnPtr], 1, RowPointer));
                        }
                        Content.Append("\r\n</tr>");
                    }
                }
                //
                // ----- End Table
                //
                Content.Append(Constants.kmaEndTable);
                result += Content.ToString();
                //
                // ----- Post Table copy
                //
                PageCount = 1;
                if (DataRowCount > 0) {
                    PageCount = 1 + cp.Utils.EncodeInteger(((double)DataRowCount-1.0) / (double)ReportPageSize);
                }
                //if ((DataRowCount / (double)ReportPageSize) != Math.Floor((DataRowCount / (double)ReportPageSize))) {
                //    PageCount = 1 + encodeInteger(((double)DataRowCount / (double)ReportPageSize) + 0.5);
                //} else {
                //    PageCount = 1 + encodeInteger(DataRowCount / (double)ReportPageSize);
                //}
                if (PageCount > 1) {
                    result += "<div class=\"small mt-4\">Page " + ReportPageNumber + " (Row " + (RowBAse) + " of " + DataRowCount + ")</div>";
                    if (PageCount > 20) {
                        pagePtr = ReportPageNumber - 10;
                    }
                    if (pagePtr < 1) {
                        pagePtr = 1;
                    }
                    if (PageCount > 1) {
                        result += "<div class=\"small my-4\">Go to Page ";
                        if (pagePtr != 1) {
                            WorkingQS = cp.Doc.RefreshQueryString;
                            WorkingQS = modifyQueryString(WorkingQS, "GotoPage", "1", true);
                            result += "<a class=\"mx-1 p-2 border\" href=\"?" + WorkingQS + "\">1</A>...&nbsp;";
                        }
                        WorkingQS = cp.Doc.RefreshQueryString;
                        WorkingQS = modifyQueryString(WorkingQS, RequestNamePageSize, ReportPageSize.ToString(), true);
                        while ((pagePtr <= PageCount) && (LinkCount < 20)) {
                            if (pagePtr == ReportPageNumber) {
                                result += "<span class=\"mx-1 p-2 border\" >" + pagePtr + "</span>";
                            } else {
                                WorkingQS = modifyQueryString(WorkingQS, RequestNamePageNumber, pagePtr.ToString(), true);
                                result += "<a class=\"mx-1 p-2 border\" href=\"?" + WorkingQS + "\">" + pagePtr + "</A>";
                            }
                            pagePtr++;
                            LinkCount++;
                        }
                        if (pagePtr < PageCount) {
                            WorkingQS = modifyQueryString(WorkingQS, RequestNamePageNumber, PageCount.ToString(), true);
                            result += "...<a class=\"mx-1 p-2 border\" href=\"?" + WorkingQS + "\">" + PageCount + "</A>";
                        }
                        if (ReportPageNumber < PageCount) {
                            WorkingQS = modifyQueryString(WorkingQS, RequestNamePageNumber, (ReportPageNumber + 1).ToString(), true);
                            result += "...<a class=\"mx-1 p-2 border\" href=\"?" + WorkingQS + "\">next</A>";
                        }
                        result += "</div>";
                    }
                }
                return PreTableCopy + result + PostTableCopy;
                ////
                //result = ""
                //+ PreTableCopy + "<table border=0 cellpadding=0 cellspacing=0 width=\"100%\"><tr><td style=\"padding:10px;\">"
                //+ result + "</td></tr></table>"
                //+ PostTableCopy + "";
            } catch (Exception) {
                throw;
            }
            //return result;
        }
