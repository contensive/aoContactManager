        //
        //====================================================================================================
        //   Report Cell Header
        //       ColumnPtr       :   0 based column number
        //       Title
        //       Width           :   if just a number, assumed to be px in style and an image is added
        //                       :   if 00px, image added with the numberic part
        //                       :   if not number, added to style as is
        //       align           :   style value
        //       ClassStyle      :   class
        //       RQS
        //       SortingState
        //
        public static string getReport_CellHeader( int ColumnPtr, string Title, string Width, string Align, string ClassStyle, string RefreshQueryString, SortingStateEnum SortingState) {
            try {
                if (string.IsNullOrEmpty(Title)) Title = "&nbsp;";
                string Style = "VERTICAL-ALIGN:bottom;";
                if (!string.IsNullOrEmpty(Align)) Style += "TEXT-ALIGN:" + Align + ";";
                if (!string.IsNullOrEmpty(Width)) Style = Style + "width:" + Width + ";";
                string QS = "";
                switch (SortingState) {
                    case SortingStateEnum.SortableNotSet:
                        QS = modifyQueryString(RefreshQueryString, "ColSort", ((int)SortingStateEnum.SortableSetAZ).ToString(), true);
                        QS = modifyQueryString(QS, "ColPtr", ColumnPtr.ToString(), true);
                        Title = "<a href=\"?" + QS + "\" title=\"Sort A-Z\" class=\"ccAdminListCaption\">" + Title + "</a>";
                        break;
                    case SortingStateEnum.SortableSetza:
                        QS = modifyQueryString(RefreshQueryString, "ColSort", ((int)SortingStateEnum.SortableSetAZ).ToString(), true);
                        QS = modifyQueryString(QS, "ColPtr", ColumnPtr.ToString(), true);
                        Title = "<a href=\"?" + QS + "\" title=\"Sort A-Z\" class=\"ccAdminListCaption\">" + Title + "<i title=\"down\" class=\"fas fa-arrow-circle-up\"></i>";
                        break;
                    case SortingStateEnum.SortableSetAZ:
                        QS = modifyQueryString(RefreshQueryString, "ColSort", ((int)SortingStateEnum.SortableSetza).ToString(), true);
                        QS = modifyQueryString(QS, "ColPtr", ColumnPtr.ToString(), true);
                        Title = "<a href=\"?" + QS + "\" title=\"Sort Z-A\" class=\"ccAdminListCaption\">" + Title + "<i title=\"down\" class=\"fas fa-arrow-circle-down\"></i>";
                        break;
                }
                return "<td style=\"" + Style + "\" class=\"" + ClassStyle + "\">" + Title + "</td>";
            } catch (Exception) {
                throw;
            }
        }
